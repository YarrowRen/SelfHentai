const token = computed(() => route.params.token)
const pageNumber = computed(() => parseInt(route.params.page) || 1)

// Áä∂ÊÄÅÁÆ°ÁêÜ
const loading = ref(false)
const error = ref(null)
const fullImageUrl = ref('')
