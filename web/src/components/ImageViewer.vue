// ÂõæÁâáÂºïÁî®
const fullImageRef = ref(null)

// ÁøªËØëÂäüËÉΩÊï∞ÊçÆ
const screenshotData = ref(null)
const screenshotWidth = ref(0)
const screenshotHeight = ref(0)
const screenshotLoading = ref(false)

// Êà™ÂõæÈÄâÊã©Áõ∏ÂÖ≥
